package fr.analogon.r2t.pojo;

public class Bascule 
{
	private String deAnnee= "";		
	private String versAnnee= "";
	private String dateBascule = "";
	private String pourcentage ="";
	/**
	 * @return the dateBascule
	 */
	public final String getDateBascule() {
		return dateBascule;
	}
	/**
	 * @param dateBascule the dateBascule to set
	 */
	public final void setDateBascule(String dateBascule) {
		this.dateBascule = dateBascule;
	}
	/**
	 * @return the deAnnee
	 */
	public final String getDeAnnee() {
		return deAnnee;
	}
	/**
	 * @param deAnnee the deAnnee to set
	 */
	public final void setDeAnnee(String deAnnee) {
		this.deAnnee = deAnnee;
	}
	/**
	 * @return the poucentage
	 */
	public final String getPourcentage() {
		return pourcentage;
	}
	/**
	 * @param poucentage the poucentage to set
	 */
	public final void setPourcentage(String pourcentage) {
		this.pourcentage = pourcentage;
	}
	/**
	 * @return the versAnnee
	 */
	public final String getVersAnnee() {
		return versAnnee;
	}
	/**
	 * @param versAnnee the versAnnee to set
	 */
	public final void setVersAnnee(String versAnnee) {
		this.versAnnee = versAnnee;
	}

}
