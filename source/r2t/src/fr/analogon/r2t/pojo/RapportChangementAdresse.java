package fr.analogon.r2t.pojo;

public class RapportChangementAdresse {

	String idBatch;
	String dateCreation;
	String aCreeFichier;

	public String getDateCreation() {
		return dateCreation;
	}

	public void setDateCreation(String dateCreation) {
		this.dateCreation = dateCreation;
	}

	public String getIdBatch() {
		return idBatch;
	}

	public void setIdBatch(String idBatch) {
		this.idBatch = idBatch;
	}

	public String getACreeFichier() {
		return aCreeFichier;
	}

	public void setACreeFichier(String creeFichier) {
		aCreeFichier = creeFichier;
	}
	
}
